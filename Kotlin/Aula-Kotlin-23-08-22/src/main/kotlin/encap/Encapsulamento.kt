/*
    São palavras que podem proteger o meu código, seja uma função, um atributo, uma classe:

    public - Define que a função ou atributo (e etc...) será publico
             (ou seja, qualquer arquivo consegue ter acesso).


    private - Define que a função ou atributo (e etc...) será privado
              (ou seja, o único que terá acesso é a classe (ou arquivo) onde foi declarado).
 */